
class Basics
{
    static void Main()
    {
        // Print messages line by line
        System.Console.WriteLine("Welcome");
        System.Console.WriteLine("Leon");
        System.Console.WriteLine("to C#");

        // Print messages side by side
        System.Console.Write("Welcome ");
        System.Console.Write("Leon ");
        System.Console.Write("to C#");

        // Wait for pressing some key on the keyboard
        System.Console.ReadKey();

        // Clear the screen
        System.Console.Clear();

        System.Console.WriteLine("Thank You!");
        System.Console.ReadKey();
    }
}
